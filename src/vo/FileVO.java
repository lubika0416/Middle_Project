package vo;

import java.util.Date;

public class FileVO {
	
	private int fileId = -1;
	
	private Date fileRegdt;
	
	private String useAt;
	
	private int fileSn;
	
	private String filePath;
	
	private String fileSvNm;
	
	private String fileOrNm;
	
	private String fileExtsn;
	
	private String fileCn;
	
	private long fileSize;
	
	public FileVO() {
	}

	public int getFileId() {
		return fileId;
	}

	public void setFileId(int fileId) {
		this.fileId = fileId;
	}

	public Date getFileRegdt() {
		return fileRegdt;
	}

	public void setFileRegdt(Date fileRegdt) {
		this.fileRegdt = fileRegdt;
	}

	public String getUseAt() {
		return useAt;
	}

	public void setUseAt(String useAt) {
		this.useAt = useAt;
	}

	public int getFileSn() {
		return fileSn;
	}

	public void setFileSn(int fileSn) {
		this.fileSn = fileSn;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getFileSvNm() {
		return fileSvNm;
	}

	public void setFileSvNm(String fileSvNm) {
		this.fileSvNm = fileSvNm;
	}

	public String getFileOrNm() {
		return fileOrNm;
	}

	public void setFileOrNm(String fileOrNm) {
		this.fileOrNm = fileOrNm;
	}

	public String getFileExtsn() {
		return fileExtsn;
	}

	public void setFileExtsn(String fileExtsn) {
		this.fileExtsn = fileExtsn;
	}

	public String getFileCn() {
		return fileCn;
	}

	public void setFileCn(String fileCn) {
		this.fileCn = fileCn;
	}

	public long getFileSize() {
		return fileSize;
	}

	public void setFileSize(long fileSize) {
		this.fileSize = fileSize;
	}

	@Override
	public String toString() {
		return "FileVO [fileId=" + fileId + ", fileRegdt=" + fileRegdt + ", useAt=" + useAt + ", fileSn=" + fileSn
				+ ", filePath=" + filePath + ", fileSvNm=" + fileSvNm + ", fileOrNm=" + fileOrNm + ", fileExtsn="
				+ fileExtsn + ", fileCn=" + fileCn + ", fileSize=" + fileSize + "]";
	}
	
	
	
}
